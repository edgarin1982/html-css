const color = prompt("Ingrese un color");

switch (color) {
    case "rojo" :
        alert("Es rojo") 
        
        break;
    case "amarillo" :
        alert("Es amarillo") 
        
        break;
    case "negro" :
        alert("Es negro") 
        
        break;
    case "dorado" :
        alert("Es dorado") 
        
        break;

    default:
        alert('Ninguna coincidencia')
        break;
}